class Product:
    def __init__(self, id, name, price, description, image, clearance, quantity, stock, url, category, openPackage):
        self.id = id
        self.name = name
        self.price = price
        self.description = description
        self.image = image
        self.clearance = clearance
        self.quantity = quantity
        self.stock = stock
        self.url = url
        self.category = category
        self.openPackage = openPackage